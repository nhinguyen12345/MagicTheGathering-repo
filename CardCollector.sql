######## TABLE DEFINITIONS ########
# due to FK constraints create master tables first 
# and their dependents later
#table UserProfile
create table UserProfile(
UserID int primary key,
Username varchar(255),
UserPassword varchar(255),
UserRole int);

#table CardSet
create table CardSet(
SetId int primary key,
SetName varchar(255)
);
#table CardMarket
create table CardMarket(
CardID int primary key,
CardName varchar(255),
Color varchar(255),
CardType varchar(255),
Cost decimal(5,2),
SetID int,
foreign key(SetID) references CardSet(SetID) 
);
#table CardCollection
create table CardCollection(
CollectionID int primary key,
CardId int,
UserID int,
Quantity int,
foreign key(CardID) references CardMarket(CardID), 
foreign key(UserID) references UserProfile(UserID) on delete cascade on update cascade
);
#table UserDecks
create table UserDecks(
DeckID int primary key,
DeckName varchar(255) unique,
UserID int,
foreign key(UserID) references UserProfile(UserID) on delete cascade on update cascade
);
#table CommanderDecks
create table CommanderDecks(
UserID int,
DeckID int,
DeckName varchar(255),
CommanderID int,
foreign key(DeckID) references UserDecks(DeckID) on delete cascade on update cascade,
foreign key(DeckName) references UserDecks(DeckName) on delete cascade on update cascade,
foreign key(CommanderID) references CardMarket(CardID),
foreign key(UserID) references UserProfile(UserID) on delete cascade on update cascade,
primary key(UserID, DeckID)
);
#table Wishlist
create table Wishlist(
UserID int,
CardID int,
RankPreference varchar(255),
primary key(UserID, CardID),
foreign key(UserID) references UserProfile(UserID) on delete cascade on update cascade,
foreign key(CardID) references CardMarket(CardID)
);
#table CardsInDecks 75 76 90
create table CardsInDecks(
UserID int,
DeckID int,
CollectionID int,
Quantity int,
primary key(DeckID, CollectionID, UserID),
foreign key(DeckID) references UserDecks(DeckID) on delete cascade on update cascade,
foreign key(CollectionID) references CardCollection(CollectionID) on delete cascade on update cascade,
foreign key(UserID) references UserProfile(UserID) on delete cascade on update cascade
);

