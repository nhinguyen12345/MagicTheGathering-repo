    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed

        UserProfile UP = (UserProfile)cbxUserId.getSelectedItem();
        int UserId = UP.getUserId();
        CardMarket CM = (CardMarket)cbxCardId.getSelectedItem();
        int CardId = CM.getCardId();
        String RankPreference = txtRP.getText();
        int ret = new WishlistHandler().updateCard(UserId, CardId, RankPreference);
        if (ret!=-1){
            doClose(RET_OK);
        }else{
            JOptionPane.showMessageDialog(this, "Failed to update Card");
        }
    }//GEN-LAST:event_okButtonActionPerformed

