    private void populateWishlist(){
        users = new UserHandler().getUser();
        cards = new CardMarketHandler().getCard();
        ComboBoxModel cbxModel = new DefaultComboBoxModel(users.toArray());
        ComboBoxModel cbxModel2 = new DefaultComboBoxModel(cards.toArray());
        cbxUserId.setModel(cbxModel);
        cbxCardId.setModel(cbxModel2);
    }
    private Wishlist wl;
    public void setCard(Wishlist wl){
        this.wl = wl;
        txtRP.setText(String.valueOf(wl.getRankPreference()));
        cards.forEach(se -> {
            if (se.getCardId() == wl.getCardId()) {
            cbxCardId.setSelectedItem(se);
            }});
        users.forEach(se -> {
            if (se.getUserId() == wl.getUserId()) {
            cbxUserId.setSelectedItem(se);
            }
        });
    }
  
   
