    private void tblCardsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCardsMouseClicked
       //know if it is double click
       if(evt.getClickCount()==2){
           //get the selected card
           CardMarket Cardmarket = CardMarket.get(tblCards.getSelectedRow());
           FrmUpdateCardMarket frmUpdateCardMarket = new FrmUpdateCardMarket(null, true);
           frmUpdateCardMarket.setCard(Cardmarket);
           frmUpdateCardMarket.setVisible(true);
           if (frmUpdateCardMarket.getReturnStatus() == FrmUpdateCardMarket.RET_OK){
               refreshTable();
           }
       }
    }//GEN-LAST:event_tblCardsMouseClicked


