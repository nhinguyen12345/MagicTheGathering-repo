    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //Get the selected row:
        int selectedRow = tblCards.getSelectedRow();
        if(selectedRow!=-1){
            int CardId = (int) tblCards.getValueAt(selectedRow, 0);//1st column
            int ret = JOptionPane.showConfirmDialog(this, String.format("Deleting card ID %d", CardId));
            if (ret == JOptionPane.OK_OPTION){
                CardMarketHandler.deleteCard(CardId);
                refreshTable();
            }
            
            
        }else{
           JOptionPane.showMessageDialog(this, "Please select card to delete");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

