    private void populateCardsinMarket() throws SQLException{
        String keyword = txtKeyWord.getText();
        CardMarket = CardMarketHandler.loadCardMarket(keyword);
        String columns[] = new String[]{"CardId", "CardName", "color", "Cardtype", "cost", "SetId"};
        DefaultTableModel tblModel = new DefaultTableModel(columns,0){
            @Override
            public boolean isCellEditable(int i, int i1){
                return false;
            }
        };    
        CardMarket.forEach((card)->{
            //Convert card to a row and add it
            tblModel.addRow(card.getRow());  
        });
        tblCards.setModel(tblModel);
      
         
    }
